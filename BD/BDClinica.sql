CREATE DATABASE CLINICA;
USE CLINICA;

CREATE TABLE PACIENTE (
  ID_PACIENTE INT AUTO_INCREMENT PRIMARY KEY,
  DNI_PACIENTE CHAR(8) UNIQUE NOT NULL,
  NOMBRES_PACIENTE VARCHAR(50) NOT NULL,
  APELLIDOS_PACIENTE VARCHAR(50) NOT NULL,
  FECHA_NACIMIENTO DATE NOT NULL,
  EDAD_PACIENTE INT NOT NULL,
  GENERO_PACIENTE CHAR(1) NOT NULL, 
  DIRECCION_PACIENTE VARCHAR(100) NOT NULL,
  CELULAR_PACIENTE CHAR(9) NOT NULL,
  CORREO_PACIENTE VARCHAR(80) UNIQUE NOT NULL,
  USERNAME VARCHAR(50) UNIQUE NOT NULL,
  PASSWORD_HASH VARCHAR(255) NOT NULL,
  FECHA_REGISTRO DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE EMPLEADO (
  ID_EMPLEADO INT AUTO_INCREMENT PRIMARY KEY,
  NOMBRES_EMPLEADO VARCHAR(50),
  APELLIDOS_EMPLEADO VARCHAR(50),
  ESPECIALIDAD VARCHAR(50),
  CELULAR_EMPLEADO CHAR(9),
  CORREO_EMPLEADO VARCHAR(80),
  USERNAME VARCHAR(50) UNIQUE NOT NULL,
  PASSWORD_HASH VARCHAR(255) NOT NULL
);

CREATE TABLE CITA (
  ID_CITA INT AUTO_INCREMENT PRIMARY KEY,
  ID_PACIENTE INT NOT NULL,
  ID_EMPLEADO INT NOT NULL,
  FECHA_CITA DATETIME NOT NULL,
  MOTIVO VARCHAR(200),
  ESTADO VARCHAR(20) DEFAULT 'PENDIENTE',
  OBSERVACIONES TEXT
);

CREATE TABLE DOCUMENTO (
  ID_DOCUMENTO INT AUTO_INCREMENT PRIMARY KEY,
  ID_PACIENTE INT NOT NULL,
  ID_CITA INT,
  TIPO_DOCUMENTO VARCHAR(50),
  DESCRIPCION TEXT,
  FECHA_DOCUMENTO DATETIME DEFAULT CURRENT_TIMESTAMP,
  ARCHIVO_URL VARCHAR(255)
);

CREATE TABLE MEDICAMENTO (
  ID_MEDICAMENTO INT AUTO_INCREMENT PRIMARY KEY,
  NOMBRE_MEDICAMENTO VARCHAR(100) NOT NULL,
  DESCRIPCION TEXT,
  STOCK INT NOT NULL,
  PRECIO DECIMAL(10,2) NOT NULL,
  FECHA_VENCIMIENTO DATE,
  LABORATORIO VARCHAR(100)
);
CREATE TABLE RECETA (
  ID_RECETA INT AUTO_INCREMENT PRIMARY KEY,
  ID_CITA INT NOT NULL,
  ID_MEDICAMENTO INT NOT NULL,
  DOSIS VARCHAR(100),
  INDICACIONES VARCHAR(200)
);

-- RELACIONES

ALTER TABLE CITA
ADD CONSTRAINT FK_CITA_PACIENTE
FOREIGN KEY (ID_PACIENTE) REFERENCES PACIENTE(ID_PACIENTE);

ALTER TABLE CITA
ADD CONSTRAINT FK_CITA_EMPLEADO
FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO(ID_EMPLEADO);

ALTER TABLE DOCUMENTO
ADD CONSTRAINT FK_DOCUMENTO_PACIENTE
FOREIGN KEY (ID_PACIENTE) REFERENCES PACIENTE(ID_PACIENTE);

ALTER TABLE DOCUMENTO
ADD CONSTRAINT FK_DOCUMENTO_CITA
FOREIGN KEY (ID_CITA) REFERENCES CITA(ID_CITA);

ALTER TABLE RECETA
ADD CONSTRAINT FK_RECETA_CITA
FOREIGN KEY (ID_CITA) REFERENCES CITA(ID_CITA);

ALTER TABLE RECETA
ADD CONSTRAINT FK_RECETA_MEDICAMENTO
FOREIGN KEY (ID_MEDICAMENTO) REFERENCES MEDICAMENTO(ID_MEDICAMENTO);


